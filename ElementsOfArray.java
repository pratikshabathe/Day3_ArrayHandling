package com.bridgelabz.array_handling;

public class ElementsOfArray {

	public static void main(String[] args) {
		
		System.out.println("Element of an Array: ");
		int arr[] = new int[3];
		arr[0] = 10;
		arr[1] = 20;
		arr[2] = 30;
		
		for(int i = 0; i<arr.length; i++) {
			System.out.println(arr[i] + " ");
		}

	}

}
